package domino;

import java.util.List;

public class Team {
    private String teamName;
    private List<Player> players;
    
    public String getNameTeam() {
        return teamName;
    }
    
    public List<Player> getNamePlayerOnTheTeam() {
        return players;
    }
    
    public void setNameTeam(String teamName) {
        this.teamName = teamName;
    }
}
